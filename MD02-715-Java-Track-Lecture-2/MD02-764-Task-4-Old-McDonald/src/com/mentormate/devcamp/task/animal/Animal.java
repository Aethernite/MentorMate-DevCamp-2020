package com.mentormate.devcamp.task.animal;

/**
 * The type Animal.
 */
public abstract class Animal implements Sound {

    /**
     * Contains the type of the animal as string.
     */
    protected String type;

    /**
     * Gets type.
     *
     * @return the type of the animal
     */
    public String getType() {
        return type;
    }

}
